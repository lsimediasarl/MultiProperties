    public JWriteDialog(java.awt.Frame parent, boolean modal, Record record, MultiPropertiesTableModel model, int selectedColumn, boolean lockdown) {
        super(parent, modal);
        this.record = record;
        this.model = model;
        this.lockdown = lockdown;
        rgui = record.getGUI(model, selectedColumn, lockdown);

        initComponents();

        getContentPane().add(rgui.getVisual(), BorderLayout.CENTER);

        BT_Ok.addActionListener(this);
        BT_Cancel.addActionListener(this);

    }

    //**************************************************************************
    //*** ActionListener
    //**************************************************************************
    @Override
    public void actionPerformed(ActionEvent e) {
        if (e.getActionCommand().equals("ok")) {
            rgui.apply();

            setVisible(false);
            dispose();

        } else if (e.getActionCommand().equals("cancel")) {
            rgui.cancel();

            setVisible(false);
            dispose();
        }
    }

